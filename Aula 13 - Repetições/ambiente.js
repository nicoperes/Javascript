// While
var c = 1
while (c <= 6) {
    console.log(`Passo ${c}`)
    c++ // c = c + 1
}

// Do While
var c = 1
do {
    console.log(`Passo ${c}`)
    c++ // c = c + 1
} while (c <= 6)

